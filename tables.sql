	id SERIAL PRIMARY KEY,
	length INTEGER,
	field TEXT,
	language_id INTEGER REFERENCES languages(id),
	worktime INTEGER
);

CREATE TABLE people (
	id SERIAL PRIMARY KEY,
	name TEXT,
	available BOOLEAN
);

CREATE TABLE translators (
	language_id INTEGER REFERENCES languages(id),
	person_id INTEGER REFERENCES people(id)
);


